// Reads the IRC trivia TOML question banks and emits a typed TS module the web
// app can import. Hidden banks are excluded by default to match the bot's
// "loaded" set; pass INCLUDE_HIDDEN=1 to bundle them too.
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";
import toml from "@iarna/toml";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const projectRoot = path.resolve(__dirname, "..");

const questionsDir =
  process.env.QUESTIONS_DIR || path.resolve(projectRoot, "../trivia/questions");
const includeHidden = process.env.INCLUDE_HIDDEN === "1";
const outFile = path.resolve(projectRoot, "src/data/questions.generated.ts");

function main() {
  if (!fs.existsSync(questionsDir)) {
    console.error(`Question bank dir not found: ${questionsDir}`);
    console.error("Set QUESTIONS_DIR to point at your trivia/questions folder.");
    process.exit(1);
  }

  const files = fs
    .readdirSync(questionsDir)
    .filter((f) => f.endsWith(".toml"))
    .sort();

  const questions = [];
  const banks = [];

  for (const file of files) {
    let data;
    try {
      data = toml.parse(fs.readFileSync(path.join(questionsDir, file), "utf8"));
    } catch (err) {
      console.warn(`Skipping unparseable bank ${file}: ${err.message}`);
      continue;
    }

    if (data.hidden === true && !includeHidden) continue;
    if (!Array.isArray(data.questions)) continue;

    let count = 0;
    for (const q of data.questions) {
      const answers = Array.isArray(q.answer) ? q.answer : [q.answer];
      const cleaned = answers
        .filter((a) => typeof a === "string" && a.trim().length > 0)
        .map((a) => a.trim());
      if (!q.question || cleaned.length === 0) continue;
      questions.push({
        question: String(q.question).trim(),
        answer: cleaned,
        bank: data.name || file,
      });
      count++;
    }
    if (count > 0) banks.push({ name: data.name || file, count });
  }

  const header =
    "// AUTO-GENERATED by scripts/build-questions.mjs — do not edit by hand.\n" +
    "// Run `npm run questions` to regenerate from the TOML banks.\n" +
    'import type { Question } from "../types";\n\n';

  const body =
    "export const questions: Question[] = " +
    JSON.stringify(questions, null, 2) +
    ";\n";

  fs.mkdirSync(path.dirname(outFile), { recursive: true });
  fs.writeFileSync(outFile, header + body);

  console.log(`Wrote ${questions.length} questions from ${banks.length} banks:`);
  for (const b of banks) console.log(`  ${b.name}: ${b.count}`);
  console.log(`-> ${path.relative(projectRoot, outFile)}`);
}

main();
